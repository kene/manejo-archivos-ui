    /**
     * Metodo que se encarga de buscar los archivos de los 
     * empleados que se han sido registrados.
     */
    private void buscar(){
        JFileChooser fc = new JFileChooser(); // Es la ventana para la busqueda de archivos
        
        //Pone un directorio actual cuando se abra la ventana de archivos
        fc.setCurrentDirectory(new File(ubicacion));  
        fc.showOpenDialog(null); // abre la ventana de dialogo   
        File archivo = fc.getSelectedFile(); // archivo seleccionado 
        
        try {
            // se crea objeto reader para leer el archivo
            FileReader fr = new FileReader(archivo);
            // buffer del reader para mantener la informacion
            BufferedReader br = new BufferedReader(fr);
            
            String texto = "";
            String linea = "";
            
            // se lee el archivo linea por linea en un ciclo while
            while((linea = br.readLine()) != null){
                // cada linea del archivo se guarda en una cadena
                texto += linea + "\n"; 
            }
            
            jTxtArchivo.setText(texto); // se coloca la informacion de texto en un text area
                    
            // se manda un mensaje que el archivo fue leido correctamente
            JOptionPane.showMessageDialog(null, "Archivo leido correctamente");
        } catch (Exception e) { // si existe un error se manda un mensaje
            JOptionPane.showMessageDialog(null, "Archivo No leido correctamente");
        }
    }
    
    
