    /**
     * Metodo que se encarga de crear los empleados registrados
     * y guardarlos en un archivo de texto por empleado
     */
    private void crear(){
        String archivo = jTxtRfc.getText() + ".txt"; // se crea el nombre del archivo
        File creaUbicacion = new File(ubicacion); // crea un objeto File donde se creara el directorio del archivo
        File creaArchivo = new File(ubicacion +  archivo);  // crea el archivo del trabajador que se llenara con la informacion
        
        if(jTxtRfc.getText().equals("")){ // valida que existe un RFC
            JOptionPane.showMessageDialog(rootPane, "No hay RFC");
            
        }else{
            
            try {
                
                if(creaArchivo.exists()){ // si el archivo existe no se crea, ya que cada archivo es unico
                    JOptionPane.showMessageDialog(rootPane, "El registro ya existe");
                }else{
                    creaUbicacion.mkdirs(); // crea la carpeta dentro de la ubicacion del proyecto
                    // Objeto con el que le da formato de lo que se guardara en el archivo
                    Formatter crea = new Formatter(ubicacion + archivo); 
                    
                    // recupera la informacion de la interfaz y la guarda en el archivo
                    // con el formato que se le manda
                    crea.format("%s\r\n%s\r\n%s", "RFC = " + jTxtRfc.getText(),
                            "Nombre = " + jTxtNombre.getText(),
                            "No Empleado = " + jTxtNoEmpleado.getText());
                    
                    crea.close(); // cierra el objeto Formatter
                    // manda un mensaje que el archivo ha sido creado
                    JOptionPane.showMessageDialog(rootPane, "Archivo creado");
                    jTxtRfc.setText("");
                    jTxtNombre.setText("");
                    jTxtNoEmpleado.setText("");
                }
                
            } catch (Exception e) { // si existe una excepcion manda un mensaje
                JOptionPane.showMessageDialog(rootPane, "Archivo NO creado");
            } 
        }
    }
    
    
    
